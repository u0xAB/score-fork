package org.jpab;

public class PortAudioException extends Exception {

	private static final long serialVersionUID = 1L;

	public PortAudioException() {
		super();
	}

	public PortAudioException(String message, Throwable cause) {
		super(message, cause);
	}

	public PortAudioException(String message) {
		super(message);
	}

	public PortAudioException(Throwable cause) {
		super(cause);
	}
	
}
